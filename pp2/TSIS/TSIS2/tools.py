import collections

def flood_fill(surface, start_pos, fill_color):
    """
    BFS (Кеңінен іздеу) алгоритмі арқылы бір түсті аймақты құю (бояу)
    Алгоритм заливки области (Flood Fill) с использованием поиска в ширину (BFS)
    
    :param surface: Боялатын бет (pygame.Surface) / Поверхность для заливки
    :param start_pos: Бастапқы нүкте (x, y) / Начальная координата клика
    :param fill_color: Құйылатын жаңа түс / Новый цвет заливки
    """
    try:
        # Бастапқы нүктедегі пиксельдің түсін анықтау / Получаем цвет пикселя, по которому кликнули
        target_color = surface.get_at(start_pos)
    except IndexError:
        # Егер координаттар экраннан тыс болса, қате шықпас үшін тоқтату
        # Если координаты вне экрана, прерываем функцию во избежание ошибки
        return
        
    # Егер басылған пиксельдің түсі құйылатын түспен бірдей болса, ештеңе істемеу
    # Если цвет клика уже совпадает с цветом заливки, ничего не делаем
    if target_color == fill_color:
        return

    # Экранның (беттің) ені мен биіктігін алу / Получаем размеры поверхности
    width, height = surface.get_size()
    # BFS алгоритміне арналған кезек (queue) құру және бастапқы нүктені қосу
    # Инициализация очереди (deque) для BFS начальной точкой
    queue = collections.deque([start_pos])
    
    # Бетті тікелей өзгерту үшін құлыптау (жылдамдықты арттырады)
    # Блокировка поверхности для ускорения работы функций get_at и set_at
    surface.lock()
    try:
        while queue:
            # Кезектен бірінші элементті алу / Извлекаем первый элемент из очереди
            x, y = queue.popleft()
            
            # Координаттардың экран шекарасынан шығып кетпеуін тексеру
            # Проверка выхода координат за границы экрана
            if x < 0 or x >= width or y < 0 or y >= height:
                continue
                
            # Егер осы пиксельдің түсі бастапқы түспен бірдей болса / Если цвет пикселя совпадает с целевым
            if surface.get_at((x, y)) == target_color:
                # Оны жаңа түске бояу / Заливаем пиксель новым цветом
                surface.set_at((x, y), fill_color)
                
                # Көршілес 4 пиксельді (оң, сол, төмен, жоғары) кезекке қосу
                # Добавляем соседние 4 пикселя в очередь для последующей проверки
                queue.append((x + 1, y))
                queue.append((x - 1, y))
                queue.append((x, y + 1))
                queue.append((x, y - 1))
    finally:
        # Процесс аяқталған соң бетті құлыптан босату / Разблокировка поверхности
        surface.unlock()
