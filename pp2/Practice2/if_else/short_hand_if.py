# 1) Basic short-hand if (one-line if)
x = 10
if x > 5: print("x is greater than 5")

# 2) Short-hand if / else (ternary operator)
a = 7
b = 3
print("a is greater") if a > b else print("b is greater or equal")

# 3) Check even or odd using short-hand if
number = 4
print("Even") if number % 2 == 0 else print("Odd")

# 4) Boolean check with short-hand if
is_active = True
print("Active") if is_active else print("Inactive")

# 5) Compare values using short-hand if
x = 15
y = 20
print("x is larger") if x > y else print("y is larger")
